package tarefa_wrappers;

public class TarefaWrappers {

    public static void main(String[] args) {
        int num1 = 15;  // Variável primitiva (int) recebendo valor 15
        System.out.println(num1);  // Print da variável num1 com o valor 15

        Integer num2 = num1;  //  Conversão da variável num1 com tipo primitivo int para Integer (wrapper)
        System.out.println(num2);  // Print do tipo complexo Integer com o valor 15
    }
}
